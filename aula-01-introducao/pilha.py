# Pilha [último elemento a entrar é o primeiro a sair]
# Para seguir esta estrutura no python, utilizamos o append() para adicionar no final e o pop() para remover o último

stack = [4, 5, 6]

print(stack)

stack.append(9)
stack.append(90)
stack.append(900)

print(stack)

stack.pop()

print(stack)

stack.pop()
stack.pop()

print(stack)
